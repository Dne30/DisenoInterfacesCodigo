    private void guardarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarBtnActionPerformed
        
        SolicitudesClass  solicitudesClass = solicitudNueva.getSolicitudesClass();
        try {
            SolicitudAcciones.guardarSolicitud(solicitudesClass);
            JOptionPane.showMessageDialog(null, "Registro dado de alta correctamente.", "Alta de materias", JOptionPane.INFORMATION_MESSAGE);
            limpiarCampos();
            validate();

        } catch (SQLException ex) {
            Logger.getLogger(AltaSolicitud.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_guardarBtnActionPerformed

