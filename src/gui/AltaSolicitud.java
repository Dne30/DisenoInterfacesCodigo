    private void cargarWidget() {
        //solicitudNueva.setSize(340, 190);
        solicitudNueva.setVisible(true);
        solicitudPanel.removeAll();
        solicitudPanel.add(solicitudNueva);
        validate();
    }
    private void limpiarCampos(){
        solicitudNueva.matriculaText.setText("");
        solicitudNueva.programaText.setText("");
        solicitudNueva.materiaText.setText("");
        solicitudNueva.grupoText.setText("");
    }
    
   
